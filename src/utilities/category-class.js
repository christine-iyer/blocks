export default function setClass(div, styles){
     if(div.author === 'Laura'){
          return styles.laura
     }
     if(div.author === 'Paul'){
          return styles.paul
     }
     if(div.author === 'Leah'){
          return styles.leah
     }
     if(div.author === 'Chris'){
          return styles.chris
     }
     if(div.author === 'Other'){
          return styles.other
     }
     
}