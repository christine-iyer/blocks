export default function setClass(image, styles){
     if(image.author === 'Laura'){
          return styles.laura
     }
     if(image.author === 'Paul'){
          return styles.paul
     }
     if(image.author === 'Leah'){
          return styles.leah
     }
     if(image.author === 'Chris'){
          return styles.chris
     }
     
}